        void sort(std::vector<Move>& moves)
        {
            //Sorting: 
            //1. Bestmove from TT
            //2. Capturing moves
            //3. Other moves
            int captureIndex = 0; //Point to next move to be swapped if a capturing move is found
            for (int i = 0; i < moves.size(); i++) {
                if (moves[i].to && Enemy<IsWhite>()) {
                    //capture move => swap
                    std::swap(moves[i], moves[captureIndex]);
                    captureIndex++;
                }
            }
        }
