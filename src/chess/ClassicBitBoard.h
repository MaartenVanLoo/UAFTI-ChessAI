        __forceinline map getFile(bit sq) {
            switch (SquareOf(sq) % 8) {
            case 0: //h
                return File8;
            case 1: //g
                return File7;
            case 2: //f
                return File6;
            case 3: //e
                return File5;
            case 4: //d
                return File4;
            case 5: //c
                return File3;
            case 6: //b
                return File2;
            case 7: //a
                return File1;
            default:
                return 0;
            }
        }
